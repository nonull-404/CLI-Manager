pub struct CcSwitchProjectBadge {
    path: String,
    has_override: bool,
    provider_name: Option<String>,
    vendor_hint: Option<String>,
}

/// 纯函数：探测单个项目 settings.json 文本的供应商覆盖状态。
/// 文本缺失/损坏/env 无 ANTHROPIC_BASE_URL → (false, None)，单项容错不让整批失败；
/// 有覆盖但匹配不到供应商 → (true, None)（前端显示"自定义"）。
pub(crate) fn probe_settings_text(
    raw: Option<&str>,
    providers: &[(String, Map<String, Value>, Option<String>)],
) -> (bool, Option<String>, Option<String>) {
    let Some(raw) = raw else {
        return (false, None, None);
    };
    let parsed: Option<Value> = serde_json::from_str(raw).ok();
    let Some(project_env) = parsed.as_ref().and_then(env_object) else {
        return (false, None, None);
    };
    if env_text(project_env, "ANTHROPIC_BASE_URL").is_none() {
        return (false, None, None);
    }
    let matched_provider = providers
        .iter()
        .find(|(_, provider_env, _)| provider_matches_project_env(project_env, provider_env));
    let provider_name = matched_provider.map(|(name, _, _)| name.clone());
    let vendor_hint = matched_provider.and_then(|(_, _, vendor_hint)| vendor_hint.clone());
    (true, provider_name, vendor_hint)
}

#[tauri::command]
pub async fn ccswitch_probe_projects(
    app: tauri::AppHandle,
    project_paths: Vec<String>,
    db_path: Option<String>,
) -> Result<Vec<CcSwitchProjectBadge>, String> {
    let path = resolve_db_path(&app, db_path)?;
    let mut conn = open_db_readonly(&path).await?;
    let rows = sqlx::query(
        "SELECT name, settings_config FROM providers \
         WHERE app_type = 'claude' ORDER BY sort_index, name",
    )
    .fetch_all(&mut conn)
    .await
    .map_err(|err| format!("db_query_failed: {err}"))?;
    let _ = conn.close().await;

    let mut providers: Vec<(String, Map<String, Value>, Option<String>)> = Vec::new();
    for row in &rows {
        let map_err = |err: sqlx::Error| format!("db_query_failed: {err}");
        let name: String = row.try_get("name").map_err(map_err)?;
        let settings_config: String = row.try_get("settings_config").map_err(map_err)?;
        let parsed = parse_settings_config(&settings_config);
        let vendor_hint = parsed
            .as_ref()
            .and_then(|config| config.model.clone().or(config.base_url.clone()))
            .or_else(|| Some(name.clone()));
        let parsed_value: Option<Value> = serde_json::from_str(&settings_config).ok();
        if let Some(provider_env) = parsed_value.as_ref().and_then(env_object) {
            providers.push((name, provider_env.clone(), vendor_hint));
        }
    }

    let badges = project_paths
        .into_iter()
        .map(|project_path| {
            let settings_path = PathBuf::from(project_path.trim())
                .join(".claude")
                .join("settings.json");
            let raw = fs::read_to_string(&settings_path).ok();
            let (has_override, provider_name, vendor_hint) =
                probe_settings_text(raw.as_deref(), &providers);
            CcSwitchProjectBadge {
                path: project_path,
                has_override,
                provider_name,
                vendor_hint,
            }
        })
        .collect();

    Ok(badges)
}

