fn write_codex_profile(
    app: &tauri::AppHandle,
    codex_config_dir: Option<String>,
    runtime: &CodexProviderRuntimeConfig,
) -> Result<(), String> {
    let codex_dir = resolve_codex_config_dir(app, codex_config_dir)?;
    fs::create_dir_all(&codex_dir).map_err(|err| format!("profile_write_failed: {err}"))?;
    let profile_path = codex_dir.join(format!("{}.config.toml", runtime.profile_name));
    let tmp_path = codex_dir.join(format!("{}.config.toml.tmp", runtime.profile_name));
    fs::write(&tmp_path, &runtime.profile_text)
        .map_err(|err| format!("profile_write_failed: {err}"))?;
    replace_file_with_tmp(&tmp_path, &profile_path, "profile_write_failed")
}

fn replace_file_with_tmp(tmp_path: &Path, target_path: &Path, error_prefix: &str) -> Result<(), String> {
    if target_path.exists() {
        fs::remove_file(target_path).map_err(|err| format!("{error_prefix}: {err}"))?;
    }
    if let Err(err) = fs::rename(tmp_path, target_path) {
        let _ = fs::remove_file(tmp_path);
        return Err(format!("{error_prefix}: {err}"));
    }
    Ok(())
}

fn is_wsl_launch_shell(shell: Option<&str>) -> bool {
    if !cfg!(target_os = "windows") {
        return false;
    }
    matches!(
        shell.map(str::trim).map(str::to_ascii_lowercase).as_deref(),
        Some("wsl") | Some("bash")
    )
}

fn codex_home_for_shell(resolved_dir: &Path, shell: Option<&str>) -> String {
    let path = resolved_dir.to_string_lossy().into_owned();
    if is_wsl_launch_shell(shell) {
        return crate::wsl::windows_path_to_wsl(&path).unwrap_or(path);
    }
    path
}

fn is_cli_manager_codex_profile_candidate(path: &Path) -> bool {
    let Some(file_name) = path.file_name().and_then(|name| name.to_str()) else {
        return false;
    };
    file_name.starts_with("cli-manager-") && file_name.ends_with(".config.toml")
}

fn codex_profile_stem_from_path(path: &Path) -> Option<String> {
    let file_name = path.file_name()?.to_str()?;
    file_name.strip_prefix("cli-manager-")?;
    file_name
        .strip_suffix(".config.toml")
        .map(|_| file_name.trim_end_matches(".config.toml").to_string())
}

fn cleanup_codex_profiles_in_dir(
    codex_dir: &Path,
    keep_profile_names: &[String],
) -> Result<Vec<String>, String> {
    if !codex_dir.exists() {
        return Ok(Vec::new());
    }
    if !codex_dir.is_dir() {
        return Err("codex_config_dir_invalid".to_string());
    }

    let keep: std::collections::HashSet<String> = keep_profile_names
        .iter()
        .map(|name| name.trim())
        .filter(|name| !name.is_empty())
        .map(ToOwned::to_owned)
        .collect();
    let entries =
        fs::read_dir(codex_dir).map_err(|err| format!("profile_cleanup_failed: {err}"))?;
    let mut deleted = Vec::new();

    for entry in entries {
        let entry = entry.map_err(|err| format!("profile_cleanup_failed: {err}"))?;
        let path = entry.path();
        if !path.is_file() || !is_cli_manager_codex_profile_candidate(&path) {
            continue;
        }
        let Some(profile_name) = codex_profile_stem_from_path(&path) else {
            continue;
        };
        if keep.contains(&profile_name) {
            continue;
        }
        let Ok(text) = fs::read_to_string(&path) else {
            continue;
        };
        if !text.contains(CLI_MANAGER_CODEX_PROFILE_MARKER) {
            continue;
        }
        fs::remove_file(&path).map_err(|err| format!("profile_cleanup_failed: {err}"))?;
        deleted.push(profile_name);
    }

    Ok(deleted)
}

